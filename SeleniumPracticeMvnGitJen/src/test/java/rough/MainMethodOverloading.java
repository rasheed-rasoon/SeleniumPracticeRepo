package rough;

public class MainMethodOverloading {

	public static void main(String[] args) {
		
		
		System.out.println("main(String[] ars)");
	}

public static void main(String args) {
		
		
		System.out.println("main(String ars)");
	}
public static void main(String args1,String args2) {
	
	
	System.out.println("main(String ars,String args2)");
}


	
}
